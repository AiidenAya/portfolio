    <h2> Présentation du CERP'Air </h2>
    Le Centre de Etudes et de Recherches Psychologiques Air que se nomme le CERP’Air. Il est situé sur la
    base aérienne 705 de Tours et est dirigé par la Lieutenant-Colonel MONTERNIER. Son but est de réaliser des
    sélections à base de tests pour les 28 spécialités sensibles et critiques de l’Armée de l’Air et de l’Espace (AAE),
    c’est-à-dire celles pour lesquelles les enjeux humains/opérationnels et l’investissement financier de l’AAE sont
    très élevés, tel que les pilotes, les démineurs, ou encore les forces spéciales.
</div>
<br>
<div class="container-fluid btssio" style="padding : 10px; padding-top:25px; padding-bottom:25px">
    <div class="container-fluid divcenter" style="width:80%">
        <h2> Contexte de la mission </h2>
        Depuis maintenant 15 ans, le CERP’Air utilise six applications développées par leur centre
        informatique pour réaliser la sélection et l’évaluation des candidats à l’AAE : GENTEST2, MARS_CSAA,
        MARS_NG, MPRS, PATAO_2000 et PATAO-R. Le projet SYNAPSE sur lequel je suis amené à travailler est la
        nouvelle plateforme des processus d’évaluation qui remplacera ces SI.<br>
        Ce sont des applications créées en VBA, faisant d’elles des applications plutôt lourdes à
        l’utilisation.
        <br><br>
        C’est de cette problématique que naît le projet SYNAPSE : son but va être de regrouper grâce à une
        interface web la gestion des candidats mais aussi de permettre la réalisation des tests directement
        grâce à l’application. De plus, l’utilisation du PHP permettra de réduire les différentes étapes
        dans le traitement de données informatiques.
    </div>
</div>
