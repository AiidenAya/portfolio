        <h2> Contexte de la mission </h2>
        PlanetPress 7 est le logiciel actuellement utilisé au sein d’Henry Schein, mais devient petit à
        petit obsolète, ce qui pousse l’entreprise à se tourner vers sa nouvelle version : PlanetPress
        Connect 8.<br>
        PlanetPress 7 n’est plus supporté par son éditeur, et sa version d’OS supportée est bientôt
        obsolète. L’application tourne actuellement sur des serveurs Windows 2012 R2, qui arrivent à leur
        fin de vie en Octobre 2023. En plus de ça, l’envoi de document via PlanetPress 7 n’est plus
        sécurisé.
        <br>
        L’application étant vitale à la fois pour la vente mais aussi pour l’acheminement des colis, il est
        donc important de la garder à jour.
        Ma mission sera donc de réaliser le design en HTML, CSS et Javascript des factures et bons de
        livraisons de l’entreprise. Je réalise le design directement dans l’application PlanetPress Connect
        8 grâce a son interface pour développer directement intégrée dans le logiciel.
