    <h2> Qu'est ce que la veille technologique ?</h2>
    <br>
    <p>La veille technologique, élément de la veille stratégique, consiste à surveiller les évolutions techniques,
        les innovations dans un secteur d’activité donnée. La veille technologique comprend notamment la
        surveillance, la collecte, le partage et la diffusion d’information permettant d’anticiper ou de s’informer
        sur des changements en matière de recherche, développement, brevet, lancement de nouveaux produits,
        matériaux, processus, concepts, innovation de fabrication, etc…. Cela a pour but d’évaluer l’impact sur
        l’environnement et l’organisation.</p>
    <br>
    <p> <i style=" font-size: larger;"> Pourquoi réaliser une veille informatique ?</i>
        <br>
        Une veille informatique est utile sur beaucoup de parties d'un projet professionel: Que ca soit pour se
        tenir au courant des failles sur des logiciels ou systèmes informatique, rester informé sur les nouvelles
        <i>trends</i>, ou encore découvrir de nouveaux outils lié à son projet.
        <br>
        Pour ma part, j'ai décidé de réaliser une veille qui me permettrait d'approfondir mes connaissances dans l'UI/UX
        design, et de découvrir de nouveaux outils d'apprentissage disponible, pour suivre mon projet de devenir Web
        Designer
    </p><br>
