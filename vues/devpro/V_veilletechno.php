            <h3><a href="https://feedly.com" style="color: #567189 !important;">Feedly</a></h3>
            <p> Un des outils que j'ai personnellement utilisé pour ma veille technologique se trouve être <a
                    href="https://feedly.com" style="color: #567189 !important;">"Feedly"</a>
                <br>
                Feedly est un outil utile pour regrouper agréablement différent articles sur un thème bien
                particulier. Il
                couvre à la fois les flux francophones, mais aussi les flux anglophones.
                <br>
                <br>
                Il est possible grace a celui ci de réaliser des <i>boards</i> qui permettent de regrouper sur un même
                sujet plusieurs articles de sites ou blogs différents.
                <br> Ces boards sont utiles pour l'organisation de notre veille, et pour éviter de garder trop
                d'informations non essentielle au sujet de notre veille.
                <br>
                Voici un exemple de l'affichage de tout les sites suivis. Les articles sont classés par ordre de
                publication :du plus récent au plus ancien, et ce sur un mois.
                Je prends le temps de lire les articles que je considère les plus intéressants, et je les sauvegarde si
                je remarque une cohérence avec mon sujet et mes recherches.
