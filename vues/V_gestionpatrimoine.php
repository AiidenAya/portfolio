        <h2>Qu'est ce que la gestion du patrimoine? </h2>
        La gestion du patrimoine informatique, permet à une entreprise de prévenir les défaillances et permet aussi
        de reduire les coûts liés aux systèmes d'information (Pannes, Maintenance, Consommable, etc...). C'est un
        ensemble de tâches administratives qui sont essentielles à la gestion et à la survie d'une entreprise. Elle
        permet une vue globale de tout le système informatique et d'informations<br>
        Effectuer une gestion du patrimoine permet de regrouper un ensemble de tâches visant à entretenir, développer et
        optimiser l'ensemble des
        ressources informatiques et du système d'information de l'entreprise.</p>
