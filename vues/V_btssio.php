        <h1> BTS SIO</h1>
        <br />
        Le nouveau BTS SIO (Services Informatiques aux Organisations), créé à la rentrée 2011, remplace le BTS
        Informatique de Gestion. <br />
        Il forme les étudiants à la mise en place de solutions informatiques au sein des entreprises et comprend 2
        options : <br />
        la spécialité « Solutions d’Infrastructure, Systèmes et Réseaux » (SISR) et la spécialité « Solutions
        Logicielles et Applications Métiers » (SLAM).
