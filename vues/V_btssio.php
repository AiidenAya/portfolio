                    pme, pmi, etc...)
                    soit en tant que consultant d'une SSII (société de services d'ingénierie informatique), d'une
                    société éditrice de logiciels ou d'une société de conseils.<br />
                    Les services informatiques dont il aura la responsabilité concernent à la fois les solutions
                    techniques d'infrastructures, les applications logicielles,
                    la maintenance et l'évolution de ces solutions dans le respect des contraintes légales et
                    stratégiques - en s'appuyant sur des normes de sécurité ou des guides de bonnes pratiques.
                    <br />
                    Le BTS SIO (BTS Services informatiques aux organisations) prépare à 2 métiers distincts, représentés
                    par les 2 spécialités du diplôme :
                    <br />
                    Solutions d’infrastructure, systèmes et réseaux et solutions logicielles et applications métiers.
